package group3191.models.weather;

/**
 * This class is used to represent the coordinates.
 */
public class Coord {
    private double lon;
    private double lat;

    public double getLon() {
        return this.lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return this.lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

}
