package group3191.models.weather;

/**
 * This class is used to represent the clouds.
 */
public class Clouds {
    private int all;


    public int getAll() {
        return this.all;
    }

    public void setAll(int all) {
        this.all = all;
    }

    @Override
    public String toString() {
        return "Clouds{" + "all=" + all + '}';
    }
    
}
